package com.java.conditions;

public class SwitchCasePractice {
    public static void main(String[] args) {
        int switchCase = 1;
        switch (switchCase){
            case 1:
                System.out.println(1);
                break;
            case 2:
                System.out.println(2);
                break;
            default:
                System.out.println(3);
        }
    }
}
